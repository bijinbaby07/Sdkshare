package com.trenser.simplecalculator

object Calculator {

    fun sum(a:Int ,b: Int):Int{
        return a+b
    }

    fun diff(a :Int, b: Int):Int{
        return a-b
    }

    fun mul(a:Int,b:Int):Int{
        return a * b
    }

    fun div(a:Int,b:Int):Int{
        return a / b
    }

    fun mod(a:Int,b:Int):Int{
        return a % b
    }
}